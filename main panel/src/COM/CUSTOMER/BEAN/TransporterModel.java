package COM.CUSTOMER.BEAN;


public class TransporterModel {
	private int transporterID;
	private String transFirstName, transLastName, transEmail, transPin, transContact;

	public int getTransporterID() {
		return transporterID;
	}

	public void setTransporterID(int transporterID) {
		this.transporterID = transporterID;
	}

	public String getTransFirstName() {
		return transFirstName;
	}

	public void setTransFirstName(String transFirstName) {
		this.transFirstName = transFirstName;
	}

	public String getTransLastName() {
		return transLastName;
	}

	public void setTransLastName(String transLastName) {
		this.transLastName = transLastName;
	}

	public String getTransEmail() {
		return transEmail;
	}

	public void setTransEmail(String transEmail) {
		this.transEmail = transEmail;
	}

	public String getTransPin() {
		return transPin;
	}

	public void setTransPin(String transPin) {
		this.transPin = transPin;
	}

	public String getTransContact() {
		return transContact;
	}

	public void setTransContact(String transContact) {
		this.transContact = transContact;
	}

}
